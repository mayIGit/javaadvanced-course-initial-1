package com.busyqa.course.generics.multiple;

public class RegularPair {

	Integer k;
	String  v;
	
	public RegularPair(Integer k, String v) {
		super();
		this.k = k;
		this.v = v;
	}

	public Integer getK() {
		return k;
	}

	public void setK(Integer k) {
		this.k = k;
	}

	public String getV() {
		return v;
	}

	public void setV(String v) {
		this.v = v;
	}	
}
